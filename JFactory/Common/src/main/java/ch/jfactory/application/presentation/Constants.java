public class Constants {
    /**
     * Medium space between component groups that have to be separated cleary. I.e. groups of option boxes might be
     * separated from other buttons with this gap.
     */
    public static final int GAP_BETWEEN_GROUP = 7;
    /**
     * Largest space between components that have to be clearly distinguished. I.e. "Save" or "OK" buttons are that much
     * away from other components.
     */
    public static final int GAP_BETWEEN_REGIONS = 17;
    /**
     * Space between the first component in a panel and the left or top border.
     */
    public static final int GAP_BORDER_LEFT_TOP = 12;
    /**
     * Space between the first component in a panel and the right or bottom border. Takes into account the white border
     * of some components.
     */
    public static final int GAP_BORDER_RIGHT_BOTTOM = 11;
    /**
     * Space within groups of any kind of buttons or components that belong together.
     */
    public static final int GAP_WITHIN_GROUP = 5;
    /**
     * Space within groups of toggle buttons or components of toolbars.
     */
    public static final int GAP_WITHIN_TOGGLES = 2;
}
